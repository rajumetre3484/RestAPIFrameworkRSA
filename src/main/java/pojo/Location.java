package pojo;

public class Location 
{
	private double lat; // double accepts the both +ve and -ve so
	private double lng;

	// getters and setters

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}

}
